  ADD COLUMN IF NOT EXISTS "userId" INTEGER REFERENCES public."Users"(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS title TEXT,
  ADD COLUMN IF NOT EXISTS body TEXT,
  ADD COLUMN IF NOT EXISTS images TEXT[] NOT NULL DEFAULT ARRAY[]::TEXT[],
  ADD COLUMN IF NOT EXISTS "helpfulCount" INTEGER NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS "createdAt" TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  ADD COLUMN IF NOT EXISTS "updatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW();

ALTER TABLE public."Posts"
  ADD COLUMN IF NOT EXISTS "reviewStatus" TEXT NOT NULL DEFAULT 'approved' CHECK ("reviewStatus" IN ('pending', 'approved', 'rejected')),
  ADD COLUMN IF NOT EXISTS "reviewedAt" TIMESTAMPTZ NULL,
  ADD COLUMN IF NOT EXISTS "reviewedBy" INTEGER NULL REFERENCES public."Users"(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS "reviewNote" TEXT NULL;

CREATE TABLE IF NOT EXISTS public."PostHelpfuls" (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
