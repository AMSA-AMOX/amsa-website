-- Restructure posting feature to support:
-- - user-owned posts
-- - multiple post images
-- - "helpful" reactions

-- Archive legacy table if it exists and new table has not been created yet.
DO $$
BEGIN
  IF EXISTS (
    SELECT 1
    FROM information_schema.tables
    WHERE table_schema = 'public'
      AND table_name = 'Post'
  ) AND NOT EXISTS (
    SELECT 1
    FROM information_schema.tables
    WHERE table_schema = 'public'
      AND table_name = 'Posts'
  ) THEN
    ALTER TABLE public."Post" RENAME TO "PostLegacy";
  END IF;
END $$;

CREATE TABLE IF NOT EXISTS public."Posts" (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "userId" INTEGER NOT NULL REFERENCES public."Users"(id) ON DELETE CASCADE,
  title TEXT NOT NULL CHECK (char_length(trim(title)) > 0),
  body TEXT NOT NULL CHECK (char_length(trim(body)) > 0),
  images TEXT[] NOT NULL DEFAULT ARRAY[]::TEXT[],
  "helpfulCount" INTEGER NOT NULL DEFAULT 0 CHECK ("helpfulCount" >= 0),
  "reviewStatus" TEXT NOT NULL DEFAULT 'approved' CHECK ("reviewStatus" IN ('pending', 'approved', 'rejected')),
  "reviewedAt" TIMESTAMPTZ NULL,
  "reviewedBy" INTEGER NULL REFERENCES public."Users"(id) ON DELETE SET NULL,
  "reviewNote" TEXT NULL,
  "createdAt" TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  "updatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

