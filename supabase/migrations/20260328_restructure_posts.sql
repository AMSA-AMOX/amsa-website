CREATE TABLE IF NOT EXISTS public."ShopRedemptions" (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "userId" INTEGER NOT NULL REFERENCES public."Users"(id) ON DELETE CASCADE,
  "itemKey" TEXT NOT NULL,
  "itemLabel" TEXT NOT NULL,
  cost INTEGER NOT NULL CHECK (cost > 0),
  status TEXT NOT NULL DEFAULT 'requested' CHECK (status IN ('requested', 'approved', 'fulfilled', 'rejected')),
  note TEXT NULL,
  "createdAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_shop_redemptions_user_created_at ON public."ShopRedemptions" ("userId", "createdAt" DESC);

CREATE OR REPLACE FUNCTION public.set_posts_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW."updatedAt" = NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_posts_updated_at ON public."Posts";
CREATE TRIGGER trg_posts_updated_at
BEFORE UPDATE ON public."Posts"
FOR EACH ROW
EXECUTE FUNCTION public.set_posts_updated_at();

